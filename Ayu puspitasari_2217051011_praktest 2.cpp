#include <iostream>
using namespace std;
int main () {
	int input ;
	cin >> input ;
	if ( input == 1 ) {
		cout << " satu " ; 
	} else if ( input == 2 ) {
		cout << " dua " ;
	} else if ( input == 3 ) {
		cout << " tiga " ;
	} else if ( input >= 4 ) {
		cout << " bilangan lebih besar dari tiga " ;
		
	}
}
